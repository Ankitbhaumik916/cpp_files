#include<iostream>
int main(){
    //int stores whole numbers
    int x=6;
    int y=5;
    int sum= x+y;
    std::cout<< x <<'\n';
    std::cout<< sum <<'\n';
    //double stores decimal
    double a= 5.67;
    std::cout<<a<<'\n';
    //char stores a single value 
    char grade='S';
    std::cout<< grade <<'\n';
    //bool (true or false)
    bool student= true;
    bool power= true;
    bool forsale= false;
    //string (sequence of txt)
    std::string name= "Bro";
    std::cout<<"hello my name is "<< name<<'\n';
return 0;
}
